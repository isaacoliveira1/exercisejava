package Pacotes;

public class Escova {
	double pc;
	String cor;
	int tm;
	String fn;
	
	
	
	public void preço() {
		System.out.println("Preço: " +pc);
	}
	
	public void cor() {
		System.out.println("Cor: " +cor);
	}
	
	public void tamanho() {
		System.out.println("Tamanho: " +tm);
	}
	
	public void função() {
		System.out.println("Função da Escova: "+fn);
	}
}
